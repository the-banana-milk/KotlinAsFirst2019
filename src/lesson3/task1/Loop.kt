 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    var number = n
    var rememberNum = n
    var tenInPower = 1
    var newNum = 0
    while (number != 0) {
        number /= 10
        tenInPower *= 10
    }
    while (rememberNum != 0) {
        newNum += (rememberNum % 10) * tenInPower
        tenInPower /= 10
        rememberNum /= 10
    }
    return newNum / 10
}

